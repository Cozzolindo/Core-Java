package DataTypes;

public class explicitCasting {
    
    public static void main(String[] args) {
        int i = 100;
        byte b = (byte)i;

        int k = 97;
        char ch = (char)k;

        System.out.println(b);
        System.out.println(ch);

        int y = 130;
        byte z = (byte)y;

        System.out.println(z);
    }
}
